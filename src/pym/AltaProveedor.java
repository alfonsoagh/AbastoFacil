    private void agregarPActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_agregarPActionPerformed
    {//GEN-HEADEREND:event_agregarPActionPerformed
        boolean b = false;
        if (nombrePrv.getText().isEmpty())
        {
            b = true;
            nomProErr.setText("Rellenar Campo");
            nomProErr.setVisible(true);
        }
        if (telefono.getText().isEmpty())
        {
            b = true;
            telefonoErr.setText("Rellenar Campo");
            telefonoErr.setVisible(true);
        }
        if (direccion.getText().isEmpty())
        {
            b = true;
            direErr.setText("Rellenar Campo");
            direErr.setVisible(true);
        }
        if (!b)
        {
            String valores = "DEFAULT, '" + nombrePrv.getText() + "', '" + telefono.getText()
                    + "', '" + direccion.getText() + "'";
            switch (CRUD.create("Proveedor", "", valores, "Proveedor"))
            {
                case 0:
                    JOptionPane.showMessageDialog(null, "Proveedor REGISTRADO");
                    nombrePrv.setText("");
                    telefono.setText("");
                    direccion.setText("");
                    break;
                case 1:
                    
                    break;
                case 2:
                    JOptionPane.showMessageDialog(null, "Proveedor NO REGISTRADO \n"
                            + "El nombre ya se encuentra registrado");
                    break;
            }
        } else
        {
            JOptionPane.showMessageDialog(null, "Proveedor NO REGISTRADO");
        }

    }//GEN-LAST:event_agregarPActionPerformed

