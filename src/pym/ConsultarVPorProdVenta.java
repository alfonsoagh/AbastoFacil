    private void buscarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_buscarActionPerformed
    {//GEN-HEADEREND:event_buscarActionPerformed
        if (!ticket.getText().isEmpty())
        {
            jScrollPane1.setVisible(true);
            jTable1.setRowHeight(32);
            m2 = new DefaultTableModel();
            String c2[] =
            {
                "Clave", "Nombre", "Cantidad", "Precio Unitario", "Total"
            };
            m2.setColumnIdentifiers(c2);
            jTable1.setModel(m2);
            try
            {
                ResultSet rat = CRUD.read("Venta_Producto", "*", "WHERE id_Venta=" + ticket.getText());
                while (rat.next())
                {
                    ResultSet ratP = CRUD.read("Producto", "*", "WHERE id=" + rat.getString("id_Producto"));
                    ratP.next();
                    String f[] =
                    {
                        ratP.getString("id"), ratP.getString("nombre"),
                        rat.getString("cantidad"), ratP.getString("precio"), rat.getString("total")
                    };
                    m2.addRow(f);

                }
            } catch (Exception e)
            {
                System.out.println(e.toString());
            }
            jTable1.setEnabled(false);
            if (jTable1.getRowCount()==0)
            {
                jScrollPane1.setVisible(false);
                JOptionPane.showMessageDialog(null, "No se encontr√≥ la venta");
            }
        } else
        {
            tickErr.setVisible(true);
            JOptionPane.showMessageDialog(null, "Ingrese una venta");
        }
    }//GEN-LAST:event_buscarActionPerformed


